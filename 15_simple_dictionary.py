# Difficulty Level: Beginner

# Question: Create a dictionary that contains the keys a  and b  and their
# respective values 1  and 2 .

# Hint: Dictionaries can be created either by using curly brackets or by using
# the dict  function.

# Program
# Solution 1
simple_dict = {'a':1, 'b':2}
print(simple_dict)

# Solution 2
simple_dict = dict(a = 1, b = 2)
print(simple_dict)

